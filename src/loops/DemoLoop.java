package loops;

public class DemoLoop {


    public static void main(String[] args) {

   /*     LOOPS
        for(initilization ;    condition;  increment/decrement){
              code to be executed
        }*/
//INCREMENT
        for (int i=0; i<5 ;  i++ ){

            System.out.println("Hello World "+i);
        }

//DECREMENT
      for (int i=5; i>0; i--) {

          System.out.println("Decrement Hello World " + i);
      }


    }
}
